"""
线性插值
"""
import numpy as np

def _2_1(X, Y, x):
    '''
    X, Y:给出的点
    x:所要求的插值下标
    return：
    y:插值
    '''
    y = ((x - X[0]) / (X[1] - X[0]))*Y[1] + ((x - X[1]) / (X[0] - X[1]))*Y[0]
    return y

